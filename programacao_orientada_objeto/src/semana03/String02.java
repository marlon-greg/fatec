package semana03;

public class String02 {

    public static void main(String[] args) {
        String aula = "Programação Orientada a Objetos";

        // chatAt Recebe um INTEIRO de uma STRING e devolve o CHAT
        char letra = aula.charAt(0);

        System.out.println(letra);
        
        System.out.println(aula.charAt(0));
        System.out.println(aula.charAt(1));
        System.out.println(aula.charAt(2));
    }   
}
